                     <h3 class="iq-title">RESTful API</h3>
                     <p class="iq-title-desc">This documentation helps you implement the RESTful VoiceTech API. This API uses a JSON format for output. The API is stateless – all requests are validated against an API token. The API token can be obtained manually from the VoiceTech app.</p>


                     <h5 class="iq-title mr-ap">VoiceTech API token</h5>
                     <p class="iq-title-desc">It is very simple to get an authentication token. Login to your VoiceTech panel. Go to Manage -> API integration -> Public API token</p>

                    <h5 class="iq-title mr-ap">URL Naming</h5>
                     <p class="iq-title-desc">The API uses a straight-forward URL naming convention. Each request must be made to the API endpoint (https://developers.voicetech.co). API token must be provided as part of the query string for all requests, using the 'token' variable, e.g. https://developers.VoiceTech.co?token=tS5adsXC6V2nH991</p>

                    <h5 class="iq-title mr-ap">Response Format</h5>
                     <p class="iq-title-desc">Each response sent from the API made in json format contains the 'status' and status response parameters.</p>
                     <p class="iq-title-desc">When the 'status' is 'success', the status response parameters contain the 'code' and an array of data, if request is POST or GET. The status response parameter contains the success message if it is a PUT request.</p>
                      <p class="iq-title-desc">In case the 'status' is 'error', the response always contains the error 'code' and 'message' in the status response parameter.</p>

